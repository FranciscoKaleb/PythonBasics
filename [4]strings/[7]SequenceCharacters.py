

#[1] \n - nextline - brings the cursor to the next line
print('-----------------[1]------------------')
print('hello \nWorld')

#[2] \r - carriage return - brings the cursor to the beginning of the current line
print('-----------------[2]------------------')
print('hello \rworld')

#[3] \t tab
print('-----------------[3]------------------')
print('\thello world')

#[4] \b backspace
print('-----------------[4]------------------')
print('hello\b\b world')

#[5] \\ double backslash
print('-----------------[5]------------------')
print('hello world\\')












